listn = []
while(True):
    m = input("введите команду help или други команды")
    if m == "help":
        print("доб=добавить \nуд=удалить по номеру \nпок=показать список \nред=редактировать")
    elif m == "доб": 
        listn.append(input("что добавить?"))
    elif m == "уд":
        listn.pop(int(input("что удалить?")))
    elif m == "пок":
        print(listn)
    elif m == "ред":
        num = int(input("какое число редактировать?"))
        peremena = input("на какое слово заменмть?")
        listn[num] = peremena
    elif m == "ред2":
        kj = input("какое слово для замены???????????????????????????????????????????????????????????????????????????????????????")
        if kj in listn:
            pр = input("на какое слово????????????")
            for step in range(len(listn)):
                if listn[step] == kj:
                    listn[step] = pр                 
        else:
            print("такого нет слово")
            